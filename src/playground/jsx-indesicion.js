// This is JSX = JAVASCRIPT XML

//Template One
const app = {
  title: 'Indesicion App',
  subtitle: 'Put your life in the hands of a computer',
  options: []
}

//removes all notes form app.options array
const onRemoveAll = (e) => {
  e.preventDefault

  app.options = []

  renderApp()
}

// Form Submission
const onFormSubmit = (e) => {
  e.preventDefault()

  const option = e.target.elements.option.value

  if (option) {
    app.options.push(option)
    e.target.elements.option.value = ''
  }
  // consle.log(app.options.length)
  renderApp()
}


// Chooses an option based on a random number generated by clicking a button
const onMakeDescision = () => {
  const randomNum = Math.floor(Math.random() * app.options.length)
  const pickedOption = app.options[randomNum]
  alert(pickedOption)
}

// impelemnts jsx in index.html
const appRoot = document.getElementById('app')


const renderApp = () => {
  const template = (
    <div>
      <h1>{app.title}</h1>
      {app.subtitle && <h2>{app.subtitle}</h2>}
      <p>{app.options.length > 0 ? "Here are your options" : "No options"}</p>
      <button disabled={app.options.length === 0} onClick={onMakeDescision}>What shall I do?</button>
      <button onClick={onRemoveAll}>Remove all options</button>
      <ol>
        {app.options.map((option) => <li key={option}>{option}</li>)}
      </ol>
      <form onSubmit={onFormSubmit}>
        <input type='text' name='option'></input>
        <button>Add option</button>
      </form>
    </div>
  )

  ReactDOM.render(template, appRoot)

}

renderApp()
